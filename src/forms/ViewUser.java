    private void userTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_userTableMouseClicked
        int index = userTable.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(null,
                    "No row selected. Please select a row first.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return; // Exit the method
        }

        TableModel model = userTable.getModel();
// The imagename is in column 6 (index 6) of your table
        String name = Objects.isNull(model.getValueAt(index, 6)) ? null : model.getValueAt(index, 6).toString();

        if (!Objects.isNull(name)) {
            // Construct the image path
            String imagePath = BDUtility.getPath("/images" + File.separator + name);
            File imageFile = new File(imagePath);

            if (imageFile.exists()) {
                // Resize and display the image
                ImageIcon icon = new ImageIcon(imagePath);
                Image image = icon.getImage().getScaledInstance(322, 286, Image.SCALE_SMOOTH);
                ImageIcon resizedIcon = new ImageIcon(image);
                lblImage.setIcon(resizedIcon);
            } else {
                // File doesn't exist
                lblImage.setIcon(null);
                JOptionPane.showMessageDialog(null,
                        "Either the image has been deleted or not found.",
                        "Image not found",
                        JOptionPane.WARNING_MESSAGE);
            }
        } else {
            // No image name available in the table
            lblImage.setIcon(null);
            JOptionPane.showMessageDialog(null,
                    "No image name found in the selected row.",
                    "No Image",
                    JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_userTableMouseClicked
