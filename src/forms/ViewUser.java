    private void fetchUser(String searchText) throws Exception {
        // Get the table model
        DefaultTableModel model = (DefaultTableModel) userTable.getModel();
        model.setRowCount(0); // Clear the table

        try {
            // Establish a connection
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();

            // Define the query
            String query;
            if (Objects.isNull(searchText)) {
                query = "SELECT * FROM userdetails";
            } else {
                query = "SELECT * FROM userdetails WHERE name LIKE '%" + searchText + "%' OR email LIKE '%" + searchText + "%'";
            }

            // Execute the query
            ResultSet rs = st.executeQuery(query);

            // Populate the table
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("name"),
                    rs.getString("gender"),
                    rs.getString("email"),
                    rs.getString("contact"),
                    rs.getString("uniquergid"),
                    // rs.getString("imagename"),
                    rs.getString("session"),
                    rs.getString("year"),
                    rs.getString("semester"), // rs.getString("subcode")
                });
            }
        } catch (Exception ex) {
            // Handle exceptions
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
        }
    }


